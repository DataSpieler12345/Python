#The same as | == 
r=100 == 100
print(r)

# Other than | !=
z=50 != 100
print(z)

j=100 != 100
print(j)

# Less than | <
a=50 < 100
print(a)

# Less than or equal to | <=
c=100 <= 100
print(c)

# Greater than | > 
k=100 > 99
print(k)

# Greater than or equal to | >=
f=100 >= 99.9
print(f)

l=100 >= 100.99
print(l)