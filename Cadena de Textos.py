#Cadena de textos 
cadena ="EjEmplO dE CaDENa de TExtoS"
#Convertir a minusculas
print(cadena.lower())
#Convertir a mayuscula
print(cadena.upper())
#Capitalize
print(cadena.capitalize())
#Primera letra por cada palabra en mayuscula 
print(cadena.title())
#Conversion contrario / Invierte la cadena
print(cadena.swapcase())
#Confirmar Mayusculas? con relacion a la cadena inicial
print(cadena.isupper())
#Confirmar Minusculas? con relacion a la cadena inicial 
print(cadena.islower())
#Comprobar si la cadena es numerica
#cadena ="1234"
print(cadena.isnumeric())
#Para comprobar si tiene letras y numeros y no caracteres extranos
print(cadena.isalnum())
#Es un titulo?
cadena = "Titulo 1"
print(cadena.istitle())
      